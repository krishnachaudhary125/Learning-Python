txt = "Krishna Chaudhary"

print(txt.strip())			# remove white space

print(txt.upper())			# change text char to uppercase

print(txt.split())			# split into list

print(txt.count("a"))			# count char repeatance

print(txt.lower())			# change text char to lowercase

print(txt.join(["I am ", " and I am 21 years old."]))

print(txt.find("k"))			# find substring index

print(txt.replace("a", "b"))		# replace char